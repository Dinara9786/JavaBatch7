package com.syntax.class09;

public class HomeworkPatterns {
	
	public static void main (String [] args) {
		
		for (int i=5; i>1; i--) {
								
			for (int j=5; j>=1; j--) { 
				System.out.print(j);
		}
			System.out.println();
		}
		
		System.out.println("===============5555======================");
		
		for (int i=5; i>=1; i--) {
						
			for (int j=1; j<=5; j++) { 
				System.out.print(i);
		}
			System.out.println();
		}
		
		
	}

}
